        $sql_content = file_get_contents($schema_file);
        
        // Strip UTF-8 BOM if present
        if (substr($sql_content, 0, 3) === "\xEF\xBB\xBF") {
            $sql_content = substr($sql_content, 3);
        }
        
        // Normalize line endings to LF before splitting
        $sql_content = str_replace(array("\r\n", "\r"), "\n", $sql_content);
        $queries = explode(";", $sql_content);
        
        $errCount = 0;
        foreach ($queries as $query) {
            $query = trim($query);
            if (!empty($query)) {
                try {
                    $db->exec($query);
                } catch (PDOException $e) {
                    // Silent continue for common harmless warnings
                    if(!strpos($e->getMessage(), "already exists")) $errCount++;
                }
            }
        }
        if($errCount === 0) log_msg("Schema Imported Successfully.", "success");
        else log_msg("Schema Imported with minor warnings.", "info");
        
