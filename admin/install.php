}

// Gate 2: DB meta flag (installed=1) — catches cases where lock file was deleted but DB is configured
if (!$install_blocked && isset($db) && $db) {
    try {
        $meta_check = $db->query("SELECT COUNT(*) FROM `meta` WHERE `id`=1")->fetchColumn();
        if ($meta_check > 0) {
            $install_blocked = true;
        }
    } catch (PDOException $e) {
        // Table doesn't exist yet — installer should proceed
    }
}

if ($install_blocked) {
    if (file_exists("database.php") && isset($db) && $db) {
        // Fully installed and locked — require admin auth to re-install
        header("Location: ../admin/login.php");
        exit("<h1>Installation Locked</h1><p>The system is already installed. <a href='../admin/login.php'>Go to Admin Login</a>.</p>");
    } else {
        // Lock exists but config missing (Broken state or Intentional Reset)
        echo "<div style='background:#fefce8; color:#a16207; padding:20px; border:1px solid #fde047; margin:20px; border-radius:8px;'>
